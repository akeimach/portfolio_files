    <p>Both artificial and biological systems can exhibit complex behaviors that are not captured in any individual system component, but only emerge when considering the population as whole. In biological systems, such behaviors can be much more pronounced when probing the system with its natural inputs. I develop theoretical, computational, and data-driven tools to understand how the structure of the natural world shapes the representation and transfer of information in biological populations.  I focus on a broad set of related questions that elucidate how resources are distributed across a population, how local interactions shape information flow across the population, and how the structure of interactions adapts over time and in response to stimulus variations.  I am currently investigating these questions in neural populations, where I perform both sensory-specific and integrated whole-brain studies.  To check out my work, see the Research page, or click on the link below.</p>

    
        
